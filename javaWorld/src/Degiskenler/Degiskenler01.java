package Degiskenler;

public class Degiskenler01 {

	public static void main(String[] args) {
		
		int x = 15;
		System.out.println(x); // int tam sayıları tanımlamak için kullanılır - + 8 basamağa kadar depolanabilir.
		
		double y = 10.5;
		System.out.println(y); // double ondalıklı sayılar için kullanılır
		
		char kerem = '*';
		System.out.println(kerem); // char sadece 1 karakteri tutar '' tek tırnakla tanımlanır.
		
		String metin = "Kerem Gürbüz junior yazılımcıdır.";
		System.out.println(metin); // String baş harfi büyük yazılır tanımlanırken "" çift tırnakla yazılır

	}

}
