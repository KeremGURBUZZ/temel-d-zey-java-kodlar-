package MatematikselOperatorler;

public class DegiskenlerVeMatematikselOpOrnek {

	public static void main(String[] args) {
		
		int x = 9, y = 4, z;
		
		System.out.println("x = " + x + " <-> " + "y = "+ y);
		z = y; // z-> 4;
		y = x; // y-> 9;
		x = z; // x-> 4;
		System.out.println("x = " + x + " <-> " + "y = "+ y);
		

	}

}
